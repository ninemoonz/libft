#include <libft.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	int				i;
	size_t	srclen;

	i = 0;
	srclen = ft_strlen(src);
	while (i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srclen);
}
